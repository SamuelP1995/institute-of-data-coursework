function add(a,b){
    return a+b
}
function subtract(a,b){
    return a-b
}
function multiply(a,b){
    return a*b
}
function divide(a,b){
    return a/b
}

function hello(name){
    const greeting = "Hello";
    const greetingText = greeting + " " + name;
    return greetingText
}

console.log(add(5,5))
console.log(subtract(5,5))
console.log(multiply(5,5))
console.log(divide(5,5))
console.log(hello("Samuel"))